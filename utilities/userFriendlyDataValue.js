///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// Function userFriendlyDataValue(originalValue)
// Returns the original argument, unless this argument is a blank string,
// in which case the function returns the string "Blank in Database" 
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

function userFriendlyDataValue(originalValue) {
  if (originalValue == "") {
    return "Blank in Database";
  } else {
    return originalValue;
  }
}